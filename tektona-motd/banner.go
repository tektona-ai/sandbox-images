// Lime (#CDF12E) ‚Üí dark green (#4F5D13), same gradient as tektona-cli.
var bannerColors = []string{"#CDF12E", "#A8CC26", "#84A81E", "#5F8316", "#4F5D13", "#4F5D13"}

func renderBanner() string {
	if !term.IsTerminal(int(os.Stdout.Fd())) {
		return "\n" + strings.Join(bannerLines, "\n") + "\n"
	}
	var lines []string
	for i, line := range bannerLines {
		styled := lipgloss.NewStyle().Foreground(lipgloss.Color(bannerColors[i])).Render(line)
		lines = append(lines, styled)
	}
	return "\n" + strings.Join(lines, "\n") + "\n"
}
